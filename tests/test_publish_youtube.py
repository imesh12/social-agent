def test_publisher_agent_records_failed_job_when_upload_fails() -> None:
    video = create_video("storage/videos/publish_failed_upload_video.mp4")
    db = SessionLocal()
    try:
        agent = PublisherAgent(
            db=db,
            youtube_service=FailingYouTubeService(),
        )
        job = agent.publish_youtube(video_id=video.id)

        metadata = agent.metadata_service.load_youtube_upload(video.id)
        assert job.status.value == "failed"
        assert job.youtube_video_id is None
        assert metadata["upload_status"] == "failed"
        assert "youtube unavailable" in metadata["error"]
    finally:
        db.close()
