def test_publisher_agent_runs_viral_prediction_before_upload() -> None:
    video = create_video("storage/videos/publish_prediction_video.mp4")
    llm = FakeLLMService(viral_prediction=viral_prediction(91))
    db = SessionLocal()
    try:
        agent = PublisherAgent(
            db=db,
            youtube_service=FakeYouTubeService(),
            viral_prediction_service=ViralPredictionService(llm),
        )
        job = agent.publish_youtube(video_id=video.id)

        metadata = agent.metadata_service.load_viral_prediction(video.id)
        assert job.youtube_video_id == "youtube-test-id"
        assert llm.viral_prediction_called is True
        assert metadata["viral_score"] == 91
        assert metadata["publish_recommendation"] is True
    finally:
        db.close()


