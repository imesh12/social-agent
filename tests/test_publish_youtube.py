def override_manager_agent(db: Session = Depends(get_db)) -> ManagerAgent:
    return ManagerAgent(
        db=db,
        trend_agent=TrendAgent(
            reddit_service=RedditService(),
            google_trends_service=GoogleTrendsService(),
        ),
        research_agent=ResearchAgent(llm_service=FakeLLMService()),
        script_agent=ScriptAgent(llm_service=FakeLLMService()),
        voice_agent=VoiceAgent(db=db, tts_service=TTSService()),
        video_agent=VideoAgent(
            db=db,
            ffmpeg_service=FFmpegService(),
            image_service=ImageService(),
        ),
        subtitle_agent=SubtitleAgent(db=db, subtitle_service=SubtitleService()),
        seo_agent=SEOAgent(db=db, llm_service=FakeLLMService()),
        thumbnail_agent=ThumbnailAgent(db=db, thumbnail_service=ThumbnailService()),
        publisher_agent=PublisherAgent(db=db, youtube_service=FakeYouTubeService()),
    )


def create_video(video_path: str) -> Video:
    Path(video_path).parent.mkdir(parents=True, exist_ok=True)
    Path(video_path).write_bytes(b"fake-mp4")

    db = SessionLocal()
    try:
        topic = Topic(title="AI Tools", score=90, status=TopicStatus.SCRIPTED)
        script = Script(topic=topic, content="Short script.")
        audio = Audio(script=script, path="storage/audio/publish_test_audio.mp3")
        video = Video(audio=audio, path=video_path)
        db.add(video)
        db.commit()
        db.refresh(video)
        return video
    finally:
        db.close()


def viral_prediction(score: int) -> ViralPredictionResult:
    return ViralPredictionResult(
        viral_score=score,
        predicted_ctr=score,
        predicted_retention=score,
        shareability="High" if score >= 90 else "Medium",
        uniqueness="High",
        competition="Medium",
        emotion="curiosity",
        risk_level="Low" if score >= 80 else "High",
        confidence=85,
        publish_recommendation=score >= 80,
        reasons=["strong hook"],
        improvements=["tighten thumbnail"],
        analysis_timestamp="2026-06-30T00:00:00+00:00",
        fallback_used=False,
    )


